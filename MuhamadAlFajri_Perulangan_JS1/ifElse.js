// nested if
let nilai = 69;

if (nilai >= 90) {
  console.log("Anda mendapatkan nilai A.");
} else if (nilai >= 80) {
  console.log("Anda mendapatkan nilai B.");
} else if (nilai >= 70) {
  console.log("Anda mendapatkan nilai C.");
} else {
  console.log("Anda mendapatkan nilai D.");
}


// Dalam contoh pertama, kita menggunakan if dan else untuk menguji kondisi umur.
// Jika umur lebih besar atau sama dengan 18, maka pesan "Anda dewasa" akan dicetak,
// jika tidak, pesan "Anda masih anak-anak" akan dicetak.

// Dalam contoh kedua, kita menggunakan nested if (if bersarang) untuk menguji beberapa kondisi secara berurutan.
// Program akan memeriksa kondisi pertama (nilai >= 90), jika benar maka pesan "Anda mendapatkan nilai A" akan dicetak.
// Jika tidak, program akan memeriksa kondisi kedua (nilai >= 80), dan seterusnya. Jika semua kondisi tidak terpenuhi,
// pesan "Anda mendapatkan nilai D" akan dicetak.

// Ini adalah contoh sederhana penggunaan if, else, dan nested if dalam bahasa pemrograman JavaScript.
// Anda dapat menggantinya sesuai dengan kebutuhan Anda dalam proyek pemrograman yang lebih besar.